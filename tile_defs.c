void free_tiles(t_tile *tiles) {
    for (int i=0; i<NUM_TILES; i++) {
		for (int j=0; j<tiles[i].anim_len; j++) {
			//printf("-=-= %d %d %d\n", i, j, tiles[i].anims[j].frames[0]);
			free(tiles[i].anims[j].frames);
		}
    	if (tiles[i].anim_len > 0) {
    		free(tiles[i].anims);
    	}
    }
	free(tiles);
}
